package com.example.botoxi.clases;

public class Venta {
    String id_venta,fecha_venta,cod_botella_venta;

    public Venta() {
    }

    public String getId_venta() {
        return id_venta;
    }

    public void setId_venta(String id_venta) {
        this.id_venta = id_venta;
    }

    public String getFecha_venta() {
        return fecha_venta;
    }

    public void setFecha_venta(String fecha_venta) {
        this.fecha_venta = fecha_venta;
    }

    public String getCod_botella_venta() {
        return cod_botella_venta;
    }

    public void setCod_botella_venta(String cod_botella_venta) {
        this.cod_botella_venta = cod_botella_venta;
    }
}
